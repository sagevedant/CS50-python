# Lists --> A list is a thing in which you can store a different datatypes and can call out and edit them.

friend = ["apple", "grapes", 52, True]
print(friend) # this will print the whole thing

print(friend[0]) # this will print apple

friend[1] = "oranges" # now what this thing will do is that this will change the 1st thing in the list from grapes to oranges.
print(friend[1])

# Now if we want to print some but not the whole list
print(friend[1:3]) # now this will print from grapes to true

